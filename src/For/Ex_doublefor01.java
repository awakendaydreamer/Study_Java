package For;
public class Ex_doublefor01 {
	public static void main(String[] args) {
		
		for (int i = 1; i <=5 ; i++) { //int i :1부터 5까지 반복
			for (int j = 1; j <= i; j++) { //int j :1부터 i까지 반복
				System.out.println("i 값: " + i + "\t" + "j 값 :" + j);
			}//for j
		
			
		}//for i
		
	}//main()

}//class
